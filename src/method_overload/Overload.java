package method_overload;

public class Overload {
  // method overload
	public static int sum(int a ,int b) {
		System.out.println("sum(int"+a+ " ,int "+b+")");
		return a+b;
	}
	
	public static double sum(double a,double b) {
		System.out.println("sum(double"+a+ ",double "+b+")");
		return a+b;
	}
	
	
	
	
}
